int	ft_strcmp(char *s1, char *s2)
{
	int	i;
	int	r;

	i = 0;
	r = 0;
	while (1)
	{
		if (s1[i] == '\0' && s2[i] == '\0')
			return (r);
		if (s1[i] == s2[i])
			i++;
		else
		{
			r = (int)(s1[i] - s2[i]);
			return (r);
		}
	}
}
